//! VGA Graphics Driver for NexusOS
//! Mode 13h: 320x200 @ 256 colors

/// VGA framebuffer address
const VGA_BUFFER: *mut u8 = 0xA0000 as *mut u8;

/// Screen dimensions
pub const SCREEN_WIDTH: usize = 320;
pub const SCREEN_HEIGHT: usize = 200;

/// Simple spinlock for thread-safety
pub struct Mutex<T> {
    data: core::cell::UnsafeCell<T>,
}

unsafe impl<T> Sync for Mutex<T> {}

impl<T> Mutex<T> {
    pub const fn new(data: T) -> Self {
        Self { data: core::cell::UnsafeCell::new(data) }
    }

    pub fn lock(&self) -> &mut T {
        unsafe { &mut *self.data.get() }
    }
}

/// Global screen instance
pub static SCREEN: Mutex<Screen> = Mutex::new(Screen::new());

/// Screen driver
pub struct Screen {
    _private: (),
}

impl Screen {
    pub const fn new() -> Self {
        Self { _private: () }
    }

    /// Put a pixel at (x, y) with raw color index
    #[inline]
    pub fn put_pixel(&self, x: usize, y: usize, color: u8) {
        if x < SCREEN_WIDTH && y < SCREEN_HEIGHT {
            unsafe {
                *VGA_BUFFER.add(y * SCREEN_WIDTH + x) = color;
            }
        }
    }

    /// Clear screen with color index
    pub fn clear_with_color(&self, color: u8) {
        for i in 0..(SCREEN_WIDTH * SCREEN_HEIGHT) {
            unsafe {
                *VGA_BUFFER.add(i) = color;
            }
        }
    }

    /// Draw horizontal line
    pub fn hline(&self, x: usize, y: usize, len: usize, color: u8) {
        for i in 0..len {
            self.put_pixel(x + i, y, color);
        }
    }

    /// Draw vertical line
    pub fn vline(&self, x: usize, y: usize, len: usize, color: u8) {
        for i in 0..len {
            self.put_pixel(x, y + i, color);
        }
    }

    /// Draw filled rectangle
    pub fn fill_rect(&self, x: usize, y: usize, w: usize, h: usize, color: u8) {
        for dy in 0..h {
            for dx in 0..w {
                self.put_pixel(x + dx, y + dy, color);
            }
        }
    }

    /// Draw rectangle outline
    pub fn draw_rect(&self, x: usize, y: usize, w: usize, h: usize, color: u8) {
        self.hline(x, y, w, color);
        self.hline(x, y + h - 1, w, color);
        self.vline(x, y, h, color);
        self.vline(x + w - 1, y, h, color);
    }

    /// Draw a character using 8x8 bitmap font (raw color index)
    pub fn draw_char_raw(&self, x: usize, y: usize, c: char, fg: u8, bg: u8) {
        let glyph = get_glyph(c);
        for row in 0..8 {
            for col in 0..8 {
                let pixel = (glyph[row] >> (7 - col)) & 1;
                let color = if pixel == 1 { fg } else { bg };
                self.put_pixel(x + col, y + row, color);
            }
        }
    }

    /// Draw a string (raw colors)
    pub fn draw_string_raw(&self, x: usize, y: usize, s: &str, fg: u8, bg: u8) {
        let mut cx = x;
        for c in s.chars() {
            if c == '\n' {
                continue;
            }
            self.draw_char_raw(cx, y, c, fg, bg);
            cx += 8;
        }
    }

    /// Copy one line to another (for scrolling)
    pub fn copy_line(&self, dst_y: usize, src_y: usize) {
        if dst_y < SCREEN_HEIGHT && src_y < SCREEN_HEIGHT {
            for x in 0..SCREEN_WIDTH {
                unsafe {
                    let src = *VGA_BUFFER.add(src_y * SCREEN_WIDTH + x);
                    *VGA_BUFFER.add(dst_y * SCREEN_WIDTH + x) = src;
                }
            }
        }
    }

    /// Draw a 3D button
    pub fn draw_button(&self, x: usize, y: usize, w: usize, h: usize, label: &str) {
        // Button background
        self.fill_rect(x, y, w, h, 7);
        
        // 3D effect
        self.hline(x, y, w, 15);
        self.vline(x, y, h, 15);
        self.hline(x, y + h - 1, w, 8);
        self.vline(x + w - 1, y, h, 8);

        // Center text
        let text_len = label.len() * 8;
        let text_x = x + (w.saturating_sub(text_len)) / 2;
        let text_y = y + (h.saturating_sub(8)) / 2;
        self.draw_string_raw(text_x, text_y, label, 0, 7);
    }
}

